public class DataTypes {

  public static void main(String[] args) {

    // Tipos de datos primitivos

    int myInt = 27; // Sirve para representar un entero
    System.out.println(myInt);

    double myDouble = 1.78; // Sirve para representar un numero con decimales
    System.out.println(myDouble);

    char myChar = 'a'; // Sirve para representar un caracter, se usan comillas simples
    System.out.println(myChar);

    boolean myBoolean = true;
    System.out.println(myBoolean);

    String myString = "string"; // Aunque no es un tipo primitivo, por como se usa, es un tipo principal
    System.out.println(myString);
  }
}
